    private void lanjutkanP2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lanjutkanP2ActionPerformed
        // TODO add your handling code here
        try {
            int pilihan = JOptionPane.YES_NO_OPTION;
            int hasilpilihan = JOptionPane.showConfirmDialog(this, "Anda yakin data diri sudah benar?", "PERINGATAN", pilihan);
            if (hasilpilihan == 0) {

                DataBase data = new DataBase();

                int ntahun = (int) tahun.getValue();
                int nbulan = (int) bulan.getValue();
                int nhari = (int) hari.getValue();
                int jumlah = (int) jmlorang.getValue();

                data.setDari(dari.getSelectedIndex());
                data.setKe(ke.getSelectedIndex());
                data.setHari(nhari);
                data.setBulan(nbulan);
                data.setTahun(ntahun);
                data.setJmlorang(jumlah);

                if (economy.isSelected()) {
                    data.setKursi(0);
                    setJenisKursi(0);
                } else {
                    data.setKursi(1);
                    setJenisKursi(0);
                }

                if (wolu.isSelected()) {
                    data.setJamBudal("08.00");
                } else if (loro.isSelected()) {
                    data.setJamBudal("14.00");
                } else if (pitu.isSelected()) {
                    data.setJamBudal("19.00");
                }

                data.setHarga("pesawat", data.getDari(), data.getKe());
                setJumlahOrang(jumlah);
                setHargaTiket(data.getHarga());

                data.setNama(NL.getText());
                data.setNomorIdentitas(Long.parseLong(NI.getText()));
                data.setNomorHP(Long.parseLong(NH.getText()));
                data.setEmail(NE.getText());

                PembelianSukses ps = new PembelianSukses();

                if (sisaSaldo() < 0) {
                    JOptionPane.showMessageDialog(null, "Saldo Tidak Mencukupi", "PERINGATAN", JOptionPane.WARNING_MESSAGE);
                    throw new HeadlessException();
                }

                ps.saldo(sisaSaldo());
                ps.booking();
                ps.setBudal((String) dari.getSelectedItem());
                ps.setTutuk((String) ke.getSelectedItem());
                ps.setJamBudal(data.getJamBudal());
                ps.setJamTutuk(data.getJamTutuk());
                ps.setDinoBudal(data.getHari() + " / " + data.getBulan() + " / " + data.getTahun());
                ps.setDinoTutuk(data.getHari() + " / " + data.getBulan() + " / " + data.getTahun());
                ps.setNama(data.getNama());
                ps.setNoid((String) JenisNomorIdentitas.getSelectedItem());
                ps.setNomorid(data.getNomorIdentitas());
                ps.setNotelp(data.getNomorHP());
                ps.setEmail(data.getEmail());
                ps.setHarga(getHargaTiket());
                ps.setJumlahOrang(jumlah);
                ps.setTotalHarga(getJumlahOrang() * getHargaTiket());

                if (NL.getText() == null || NE.getText() == null) {
                    throw new NullPointerException();
                }

                this.dispose();
                ps.setVisible(true);
            }
        } catch (HeadlessException | NullPointerException | NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Masukkan data dengan benar, perhatikan pengisihan huruf, angka ataupun pengisian data lainnya");
        }
    }//GEN-LAST:event_lanjutkanP2ActionPerformed

