    private void lanjutkanP1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lanjutkanP1ActionPerformed
        // TODO add your handling code here:
        try {
            int ntahun = (int) tahun.getValue();
            int nbulan = (int) bulan.getValue();
            int nhari = (int) hari.getValue();
            switch (ntahun) {
                case 2020:
                    if (nbulan == 2) {
                        if (nhari > 29) {
                            throw new Exception();
                        }
                    } else if (nbulan == 4 || nbulan == 6 || nbulan == 9 || nbulan == 11) {
                        if (nhari > 30) {
                            throw new Exception();
                        }
                    }
                    break;
                case 2014:
                    if (nbulan == 2) {
                        if (nhari > 29) {
                            throw new Exception();
                        }
                    } else if (nbulan == 4 || nbulan == 6 || nbulan == 9 || nbulan == 11) {
                        if (nhari > 30) {
                            throw new Exception();
                        }
                    }
                    break;
                default:
                    if (nbulan == 2) {
                        if (nhari > 28) {
                            throw new Exception();
                        }
                    } else if (nbulan == 4 || nbulan == 6 || nbulan == 9 || nbulan == 11) {
                        if (nhari > 30) {
                            throw new Exception();
                        }
                    }
            }
            if (economy.isSelected() || vip.isSelected()) {
                if (wolu.isSelected() || loro.isSelected() || pitu.isSelected()) {
                } else {
                    throw new Exception();
                }
            } else {
                throw new Exception();
            }
            if (dari.getSelectedIndex() == ke.getSelectedIndex()) {
                throw new Exception();
            }
            tampilawal.removeAll();
            tampilawal.add(pesawat2);
            tampilawal.repaint();
            tampilawal.revalidate();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Masukkan data dengan benar, perhatikan tanggal ataupun pengisian data lainnya");
        }
    }//GEN-LAST:event_lanjutkanP1ActionPerformed

